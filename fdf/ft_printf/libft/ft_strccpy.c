#include "libft.h"

char	*ft_strccpy(char *dest, const char *src, char c)
{
	unsigned int	i;

	i = 0;
	while (src[i] && src[i] != c)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = 0;
	return (dest);
}
